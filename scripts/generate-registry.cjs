#!/usr/bin/env node
const fs = require("fs");
const path = require("path");

const LOCALES_DIR = path.join(__dirname, "..", "src/locales");
const REGISTRY_PATH = path.join(LOCALES_DIR, "registry.ts");

const files = fs.readdirSync(LOCALES_DIR)
  .filter((f) => f.endsWith(".json"))
  .sort();

const imports = files.map((f) => {
  const name = f.replace(/\.json$/, "").replace(/[^a-zA-Z0-9]/g, "_");
  return `import ${name} from "./${f}";`;
});

const entries = files.map((f) => {
  const name = f.replace(/\.json$/, "").replace(/[^a-zA-Z0-9]/g, "_");
  const code = f.replace(/\.json$/, "");
  return `  "${code}": { translation: ${name} },`;
});

const content = `// Auto-generated by scripts/generate-registry.cjs
${imports.join("\n")}

export const localeResources: Record<string, { translation: Record<string, string> }> = {
${entries.join("\n")}
} as const;

export type LocaleCode = keyof typeof localeResources;
`;

fs.writeFileSync(REGISTRY_PATH, content, "utf-8");
console.log(`✓ Generated registry with ${files.length} locales → ${REGISTRY_PATH}`);
